# criando uma tupla 

fruta = ('uva', 'morango', 'banana', 'abacaxi', 'pera'); # cria uma tupla 

print(fruta[0]) # mostra o elemento que está na posição 0
print('_________________________________________________________________________________________________')

print(fruta.index('abacaxi') )# mostra indece do elemento abacaxi
print('_________________________________________________________________________________________________')

for i in fruta: # estou percorendo a tupla e mostrando oq tem em cada posição
    print(i)
print('_________________________________________________________________________________________________')